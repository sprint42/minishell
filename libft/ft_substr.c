#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*buf;

	if (!s)
		return (NULL);
	if (ft_strlen(s) <= start)
		return (ft_strdup(""));
	if (ft_strlen(s + start) < len)
		len = ft_strlen(s + start);
	buf = (char *)malloc((len + 1) * sizeof(char));
	if (!buf)
		return (NULL);
	ft_strlcpy(buf, s + start, len + 1);
	return (buf);
}
